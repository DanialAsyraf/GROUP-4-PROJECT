    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
        if (flag) {
        BufferedReader br = null;
        try{
            br = new BufferedReader(new FileReader("List.txt"));
            int val = Integer.parseInt(br.readLine());
            
            for(int i=0;i<val;i++){
                String ss = br.readLine();
                listModel.addElement(ss);
            }
            jList1.setModel(listModel);
            flag = false;
        }catch(Exception e){
            System.out.println(""+e);
        }finally{
            try{
            br.close();
            }catch(Exception e) {
                System.out.println(""+e);
            }
        }
        }
    }//GEN-LAST:event_jButton9ActionPerformed

    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        
        //load data into table
        if(birth.getText().equals("")||username.getText().equals("")||email.getText().equals("")||phonenum.getText().equals("")) {
            JOptionPane.showMessageDialog(this,"Please Enter All Data");
            
        }else{
            String data[] = {birth.getText(),username.getText(),email.getText(),phonenum.getText()};
            
            DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
            
            tblModel.addRow(data); //add string array data
            
            JOptionPane.showMessageDialog(this, "Your Data added Successfully");
            birth.setText("");
            username.setText("");
            email.setText("");
            phonenum.setText("");
            
        }
    }//GEN-LAST:event_jButton10ActionPerformed

